    public String obtenerFecha ( JDateChooser jdc) {
        String dia = Integer.toString(jdc.getCalendar().get(Calendar.DAY_OF_MONTH));
        String mes = Integer.toString(jdc.getCalendar().get(Calendar.MONTH) + 1);
        String anio = Integer.toString(jdc.getCalendar().get(Calendar.YEAR));
        return (anio + "/" + mes + "/" + dia);
    }
    
    public String obtenerSupermercado ( JComboBox jcb ){
        String supermercado = jcb.getSelectedItem().toString();
        return supermercado;
    }
    
