    private void onlyNum(java.awt.event.KeyEvent evt) {
        char c = evt.getKeyChar();
        if (c < '0' || c > '9') {
            evt.consume();
        }
    }
    
    private Double redondear (Double num){
        return Math.rint(num*100)/100;
    }
    
    private void onlyNumConDecimales(java.awt.event.KeyEvent evt, JTextField txtField ) {
        if (!Character.isDigit(evt.getKeyChar()) && evt.getKeyChar() != '.') {
            evt.consume();
        }
        if (evt.getKeyChar() == '.' && txtField.getText().contains(".")) {
            evt.consume();
        }
        Integer num = txtField.getText().length();
        if( num >= 2 ){
            String text = txtField.getText().substring(num-2, num);
            if(txtField.getText().contains(".")){
                if(!text.contains(".")){
                    evt.consume();                          
                }               
            }
        }        
    }
    
