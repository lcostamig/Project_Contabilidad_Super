    private Boolean obtenerCantidadesEfectivo (ArrayList<Double> NumMonedasTengo, Double Cambio, ArrayList<Integer> NumMonedasCoger){
        double cambio = redondear(Cambio);
        if (cambio <= 0.0){
            return false;
        }else{
        ArrayList<Double> Monedas = new ArrayList<Double>();
        Monedas.add(100.00);
        Monedas.add(50.00);
        Monedas.add(20.00);
        Monedas.add(10.00);
        Monedas.add(5.00);
        Monedas.add(2.00);
        Monedas.add(1.00);
        Monedas.add(0.50);
        Monedas.add(0.20);
        Monedas.add(0.10);
        Monedas.add(0.05);
        Monedas.add(0.02);
        Monedas.add(0.01);
        Integer numeroVeces = Monedas.size();
        for(int i = 0; i<numeroVeces ; i++){
            Double Moneda = Monedas.get(i);
            Double numMoneda = NumMonedasTengo.get(i);
            while (Moneda <= cambio && numMoneda > 0.0){
                cambio = cambio - Moneda;
                cambio = redondear(cambio);
                NumMonedasCoger.set(i, NumMonedasCoger.get(i) + 1);
                NumMonedasTengo.set(i, NumMonedasTengo.get(i) - 1);
                numMoneda = NumMonedasTengo.get(i);
                System.out.println("Numero moneda cogida : " + Moneda);
                System.out.println("Numero monedas que quedan : " + NumMonedasTengo.get(i));
                System.out.println("Numero monedas que cojo de " + Moneda +  " es :"+ NumMonedasCoger.get(i));
                System.out.println("Cantidad que queda : " + cambio);
                System.out.println(" *************************************************************************** ");
            }
        }  
        if(cambio == 0.0){
            return true;
        }else{
            return false;
        }
        }        
    }
    
